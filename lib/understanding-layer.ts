// 理解层转译 - 类型定义和接口
//
// 目标：将用户的困惑文本，转译为「揭示内在控制机制」的结构化理解
// 核心原则：完全没有"想太多""做太少"，但理解深度高一个维度

/**
 * 理解层转译输出结构
 *
 * 核心目标：将用户的困惑文本，转译为「揭示内在控制机制」的结构化理解
 *
 * 质量标准：
 * - 完全没有"想太多""做太少"等表面描述
 * - 理解深度比用户自述高一个维度
 * - 揭示"行动被什么机制卡住"
 */
export interface ConfusionUnderstanding {
  /**
   * 核心阻断机制
   *
   * 描述行动被什么内在机制卡住
   *
   * 示例：
   * - "行动被'必须足够确定才允许开始'的内部标准阻断"
   * - "行动被'所有事都必须亲自把控'的控制需求吞噬"
   * - "行动被'不能让别人失望'的恐惧锚定在准备阶段"
   */
  coreBlock: string;

  /**
   * 虚假策略
   *
   * 用户用来"自保"或"缓解焦虑"的方式
   * 这个策略表面上在解决问题，实际在逃避核心两难
   *
   * 示例：
   * - "用'收集更多信息'替代'做出不确定的选择'"
   * - "用'对所有事负责'替代'选择什么最重要'"
   * - "用'反复优化'替代'接受够好并推进'"
   */
  falseStrategy: string;

  /**
   * 隐形代价
   *
   * 虚假策略造成的真实代价
   *
   * 示例：
   * - "在'准备—验证—再准备'的循环中，时间被消耗，选择并未推进"
   * - "在'接活—忙不过来—继续接活'的循环中，能量被分散，核心目标零进展"
   * - "在'优化已经够好的东西'的过程中，80%的时间投入只换取了最后5%的提升"
   */
  hiddenCost: string;

  /**
   * 判定张力
   *
   * 核心两难张力，呈现为 A vs B 的形式
   * 这是后续路径判定（Narrow/Reframe/DoubleDown/Exit）的依据
   *
   * 示例：
   * - "追求确定 vs 必须选择"
   * - "对所有事负责 vs 对真正重要的目标负责"
   * - "追求完美 vs 推进到底"
   * - "满足所有期待 vs 保护自己的边界"
   */
  decisionTension: string;
}

/**
 * 理解层转译输入
 */
export interface UnderstandingInput {
  /** 用户原始困惑描述 */
  confusion: string;
  /** 场景标题（可选，用于上下文） */
  scenarioTitle?: string;
}
